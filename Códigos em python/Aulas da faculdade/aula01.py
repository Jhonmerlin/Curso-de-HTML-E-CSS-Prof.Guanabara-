#Crie um algoritmo em Python que decide se um número é positivo, negativo ou zero.
numero = int(input("Olá,digite a seguir um número e eu o verificarei para você: "))

if numero > 0:
    print("Seu número é positivo!")
if numero < 0:
    print("Seu número é negativo!")
if numero == 0:
    print("Seu número é igual a zero!")


